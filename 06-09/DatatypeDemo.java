class DatatypeDemo
{
	// calling main method
	static int number;
	public static void main(String ...args)
	{
		//primitive datatype		
		short value= (short) 856221345;
		System.out.println("the value is :" +number);	
	
		float marks =67.8f;
	    System.out.println("the value is :" +marks); 
		
		double percentage = 26.54;
	    System.out.println("the value is :" +percentage);
		
		char grade ='a';
	    System.out.println("the value is :" +grade);	
		
		long hugeNO =19651651651651L;
	    System.out.println("the value is :" +hugeNO);
			
		boolean status =true;
        System.out.println("the value is :" +status); 
		
		byte b =128;
        System.out.println("the value is :" +b);  
		
		String name =" HEllo";
        System.out.println("the value is :" +name);
	
	}
	// end of main method
	
}
